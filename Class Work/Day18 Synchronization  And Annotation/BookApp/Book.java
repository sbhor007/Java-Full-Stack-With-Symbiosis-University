package BookApp;

public class Book {
	String title;
	boolean iscomplet;
	public Book(String title) {
		super();
		this.title = title;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public boolean isIscomplet() {
		return iscomplet;
	}
	public void setIscomplet(boolean iscomplet) {
		this.iscomplet = iscomplet;
	}
	
	
	
}
